public class Nota {
    private int puntos;
    private String nota;

    public Nota(int puntos, String nota) {
        this.puntos = puntos;
        this.nota = nota;
    }

    public int getPuntos() {
        return puntos;
    }

    public String getNota() {
        return nota;
    }
}
